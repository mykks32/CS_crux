db.Employees.aggregate([
    // Match salary < 30000
    {
        $match: {
            salary: { $lt: 30000 }
            }
        },
    //    <- MATCH_OUTPUT ->
    //    +------------------------+-----------+----------+------+------+
    //    |_id                     |employee_id|manager_id|name  |salary|
    //    +------------------------+-----------+----------+------+------+
    //    |69cb77673b56083e4ce42477|1          |11        |Kalel |21241 |
    //    |69cb77673b56083e4ce42479|11         |6         |Joziah|28485 |
    //    +------------------------+-----------+----------+------+------+
    {
        $lookup: {
            from: "Employees",
            localField: "manager_id",
            foreignField: "employee_id",
            as: "manager"
            }
        },
    // <- LOOKUP_OUTPUT ->
    //    +------------------------+-----------+----------------------------------------------------------------------------------------------------------------------+----------+------+------+
    //    |_id                     |employee_id|manager                                                                                                               |manager_id|name  |salary|
    //    +------------------------+-----------+----------------------------------------------------------------------------------------------------------------------+----------+------+------+
    //    |69cb77673b56083e4ce42477|1          |[{"_id": {"$oid": "69cb77673b56083e4ce42479"}, "employee_id": 11, "name": "Joziah", "manager_id": 6, "salary": 28485}]|11        |Kalel |21241 |
    //    |69cb77673b56083e4ce42479|11         |[]                                                                                                                    |6         |Joziah|28485 |
    //    +------------------------+-----------+----------------------------------------------------------------------------------------------------------------------+----------+------+------+
    {
        $match: {
            manager: {
                $size: 0
                }
            }
        },
    //    <- MATCH_OUTPUT ->
    //    +------------------------+-----------+-------+----------+------+------+
    //    |_id                     |employee_id|manager|manager_id|name  |salary|
    //    +------------------------+-----------+-------+----------+------+------+
    //    |69cb77673b56083e4ce42479|11         |[]     |6         |Joziah|28485 |
    //    +------------------------+-----------+-------+----------+------+------+
    {
        $project: {
            _id: 0,
            employee_id: 1
            }
        },
    {
        $sort: {
            employee_id: 1
            }
        }
    // <- PROJECT_SORT_OUTPUT ->
    //    +-----------+
    //    |employee_id|
    //    +-----------+
    //    |11         |
    //    +-----------+

    ])