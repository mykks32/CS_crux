db.Insurance.aggregate([
    // tiv_2015 appears more than once
    { $group: { _id: "$tiv_2015", count: { $sum: 1 } } },
    { $match: { count: { $gt: 1 } } },
    { $project: { _id: 0, tiv_2015: "$_id" } },
    //    <- GROUP_MATCH_PROJECT_OUTPUT ->
    //    +--------+
    //    |tiv_2015|
    //    +--------+
    //    |10      |
    //    +--------+

    // get full docs with those tiv_2015
    {
        $lookup: {
            from: "Insurance",
            let: { tiv: "$tiv_2015" },
            pipeline: [
                { $match: { $expr: { $eq: ["$tiv_2015", "$$tiv"] } } }
                ],
            as: "matched_docs"
            }
        },

    //    <- LOOKUP_OUTPUT ->
    //    +--------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------+--------+
    //    |matched_docs                                                                                                                                                                                                                                                                                                                                |tiv_2015|
    //    +--------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------+--------+
    //    |[{"_id": {"$oid": "69cfdc901b63f73b026fe353"}, "pid": 1, "tiv_2015": 10, "tiv_2016": 5, "lat": 10, "lon": 10}, {"_id": {"$oid": "69cfdc901b63f73b026fe355"}, "pid": 3, "tiv_2015": 10, "tiv_2016": 30, "lat": 20, "lon": 20}, {"_id": {"$oid": "69cfdc901b63f73b026fe356"}, "pid": 4, "tiv_2015": 10, "tiv_2016": 40, "lat": 40, "lon": 40}]|10      |
    //    +--------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------+--------+

    { $unwind: "$matched_docs" },

    // $repaceRoot -> replaces the entire document with a specified nested field
    // { $replaceRoot: { newRoot: "$address" } } converts { name: "Alice", address: { city: "Kathmandu" } } into { city: "Kathmandu" }.
    { $replaceRoot: { newRoot: "$matched_docs" } },
    //    <- UNWIND_REPLACEROOT_OUTPUT ->
    //    +------------------------+---+---+---+--------+--------+
    //    |_id                     |lat|lon|pid|tiv_2015|tiv_2016|
    //    +------------------------+---+---+---+--------+--------+
    //    |69cfdc901b63f73b026fe353|10 |10 |1  |10      |5       |
    //    |69cfdc901b63f73b026fe355|20 |20 |3  |10      |30      |
    //    |69cfdc901b63f73b026fe356|40 |40 |4  |10      |40      |
    //    +------------------------+---+---+---+--------+--------+


    // keep only unique (lat, lon)
    {
        $lookup: {
            from: "Insurance",
            let: { lat: "$lat", lon: "$lon" },
            pipeline: [
                {
                    $match: {
                        $expr: {
                            $and: [
                                { $eq: ["$lat", "$$lat"] },
                                { $eq: ["$lon", "$$lon"] }
                                ]
                            }
                        }
                    },
                { $count: "loc_count" }
                ],
            as: "loc_matches"
            }
        },
    { $match: { "loc_matches.0.loc_count": 1 } },
    //    <- 2ND_LOOKUP_OUTPUT ->
    //    +------------------------+---+------------------+---+---+--------+--------+
    //    |_id                     |lat|loc_matches       |lon|pid|tiv_2015|tiv_2016|
    //    +------------------------+---+------------------+---+---+--------+--------+
    //    |69cfdc901b63f73b026fe353|10 |[{"loc_count": 1}]|10 |1  |10      |5       |
    //    |69cfdc901b63f73b026fe356|40 |[{"loc_count": 1}]|40 |4  |10      |40      |
    //    +------------------------+---+------------------+---+---+--------+--------+

    // sum tiv_2016
    { $group: { _id: 0, tiv_2016: { $sum: "$tiv_2016" } } },
    { $project: { _id: 0, tiv_2016: 1 } }
    //    <- FINAL_OUTPUT ->
    //    +--------+
    //    |tiv_2016|
    //    +--------+
    //    |45      |
    //    +--------+
    ])